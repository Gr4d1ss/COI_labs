# -*- coding: utf-8 -*-
"""
Лабораторная работа 1. Задание: работа с матрицами в NumPy.

Задание:
1. Сгенерировать две матрицы средствами NumPy
2. Вычислить средние значения в каждой строке каждой матрицы
3. К каждому значению строки первой матрицы прибавить среднее значение соответствующей строки второй матрицы
4. Проделать аналогично со второй матрицей
"""

import numpy as np

# Настройка для отображения полного интерактивного вывода (для Jupyter)
try:
    from IPython.core.interactiveshell import InteractiveShell
    InteractiveShell.ast_node_interactivity = "all"
except ImportError:
    pass  # Если IPython недоступен, просто пропускаем

# ============================================================
# Шаг 1: Генерация двух матриц
# ============================================================
print("=" * 60)
print("ШАГ 1: Генерация двух матриц")
print("=" * 60)

# Создаем генератор случайных чисел
rnd = np.random.default_rng(seed=42)  # seed для воспроизводимости

# Генерируем две матрицы размером 3x4 со случайными целыми числами от 0 до 10
matrix1 = rnd.integers(0, 10, (3, 4))
matrix2 = rnd.integers(0, 10, (3, 4))

print("\nМатрица 1:")
print(matrix1)

print("\nМатрица 2:")
print(matrix2)

# ============================================================
# Шаг 2: Вычисление средних значений в каждой строке
# ============================================================
print("\n" + "=" * 60)
print("ШАГ 2: Вычисление средних значений в каждой строке")
print("=" * 60)

# Вычисляем среднее значение по каждой строке (axis=1 - по строкам)
mean_matrix1_rows = np.mean(matrix1, axis=1)
mean_matrix2_rows = np.mean(matrix2, axis=1)

print("\nСредние значения по строкам Матрицы 1:")
print(mean_matrix1_rows)

print("\nСредние значения по строкам Матрицы 2:")
print(mean_matrix2_rows)

# ============================================================
# Шаг 3 и 4: Прибавление средних значений к матрицам
# ============================================================
print("\n" + "=" * 60)
print("ШАГ 3-4: Прибавление средних значений к матрицам")
print("=" * 60)

# Для корректного сложения нужно изменить форму векторов средних значений
# чтобы они могли транслироваться (broadcast) на матрицы
# mean_matrix2_rows имеет форму (3,), нужно (3, 1) для трансляции
mean_matrix2_rows_reshaped = mean_matrix2_rows.reshape(-1, 1)
mean_matrix1_rows_reshaped = mean_matrix1_rows.reshape(-1, 1)

# К каждой строке matrix1 прибавляем среднее соответствующей строки matrix2
result1 = matrix1 + mean_matrix2_rows_reshaped

# К каждой строке matrix2 прибавляем среднее соответствующей строки matrix1
result2 = matrix2 + mean_matrix1_rows_reshaped

print("\nРезультат: Матрица 1 + средние строк Матрицы 2:")
print(result1)

print("\nРезультат: Матрица 2 + средние строк Матрицы 1:")
print(result2)

# ============================================================
# Проверка и демонстрация работы
# ============================================================
print("\n" + "=" * 60)
print("ПРОВЕРКА (для первой строки)")
print("=" * 60)

print(f"\nПервая строка Матрицы 1: {matrix1[0]}")
print(f"Среднее первой строки Матрицы 2: {mean_matrix2_rows[0]:.2f}")
print(f"Результат: {matrix1[0]} + {mean_matrix2_rows[0]:.2f} = {result1[0]}")

print(f"\nПервая строка Матрицы 2: {matrix2[0]}")
print(f"Среднее первой строки Матрицы 1: {mean_matrix1_rows[0]:.2f}")
print(f"Результат: {matrix2[0]} + {mean_matrix1_rows[0]:.2f} = {result2[0]}")

print("\n" + "=" * 60)
print("ЗАДАНИЕ ВЫПОЛНЕНО УСПЕШНО!")
print("=" * 60)
